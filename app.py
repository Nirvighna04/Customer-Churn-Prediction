if page == "🎯 Churn Prediction":

    st.title("Customer Churn Prediction")
    st.markdown("Enter customer details below to predict whether they are likely to churn.")
    st.divider()

    try:
        model, encoders = get_artifacts()
    except Exception as e:
        st.error(f"⚠️ Could not load model artifacts. Please run the notebook first.\n\n`{e}`")
        st.stop()

    col_form, col_result = st.columns([1.2, 1], gap="large")

    with col_form:
        st.markdown('<p class="section-label">Personal Information</p>', unsafe_allow_html=True)
        c1, c2 = st.columns(2)
        with c1:
            age = st.slider("Age", min_value=18, max_value=95, value=40)
            country = st.selectbox("Country", ["France", "Germany", "Spain"])
        with c2:
            gender = st.selectbox("Gender", ["Male", "Female"])
            credit_score = st.number_input("Credit Score", min_value=300, max_value=850, value=650, step=1)

        st.markdown('<p class="section-label">Account Details</p>', unsafe_allow_html=True)
        c3, c4 = st.columns(2)
        with c3:
            tenure = st.slider("Tenure (years)", min_value=0, max_value=10, value=5)
            products_number = st.selectbox("Number of Products", [1, 2, 3, 4], index=1)
        with c4:
            balance = st.number_input("Account Balance ($)", min_value=0.0, max_value=300000.0,
                                       value=80000.0, step=1000.0)
            estimated_salary = st.number_input("Estimated Salary ($)", min_value=0.0,
                                                max_value=300000.0, value=55000.0, step=1000.0)

        st.markdown('<p class="section-label">Services</p>', unsafe_allow_html=True)
        c5, c6 = st.columns(2)
        with c5:
            credit_card = st.selectbox("Has Credit Card?", ["Yes", "No"])
        with c6:
            active_member = st.selectbox("Active Member?", ["Yes", "No"])

