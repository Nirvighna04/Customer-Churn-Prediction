MODEL_DIR = os.path.join(os.path.dirname(__file__), 'models')
DATA_PATH = os.path.join(os.path.dirname(__file__), 'data', 'churn.csv')

@st.cache_resource
def get_artifacts():
    model, encoders = load_all_artifacts(MODEL_DIR)
    return model, encoders

@st.cache_data
def load_data():
    return pd.read_csv(DATA_PATH)

@st.cache_data
def load_model_results():
    path = os.path.join(MODEL_DIR, 'model_results.csv')
    if os.path.exists(path):
        return pd.read_csv(path)
    return None

@st.cache_data
def load_feature_importance():
    path = os.path.join(MODEL_DIR, 'feature_importance.csv')
    if os.path.exists(path):
        return pd.read_csv(path)
    return None


